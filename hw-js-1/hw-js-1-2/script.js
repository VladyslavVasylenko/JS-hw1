"use strict";

let number = prompt('Введите число от 0 до 9', 0);
switch(number) {
  case '0':
    console.log('0 это символ - )');
    break;
    
  case '1':
    console.log('1 это символ - !');
    break;

  case '2':
    console.log('2 это символ - @');
    break;

  case '3':
    console.log('3 это символ - #');
    break;

  case '4':
    console.log('4 это символ - $');
    break;

  case '5':
    console.log('5 это символ - %');
    break;

  case '6':
    console.log('6 это символ - ^');
    break;

  case '7':
    console.log('7 это символ - &');
    break;

  case '8':
    console.log('8 это символ - *');
    break;

  case '9':
    console.log('9 это символ - (');
    break;
}